# Typecasting = the process of converting a variables from one data types to another
#                     str(), int(), float(), bool()

name = "Rizwan Shaikh"
age = 25
gpa = 3.2
is_student = True

print(type(name))       # <class 'str'>
print(type(age))        # <class 'int'>
print(type(gpa))        # <class 'float'>
print(type(is_student)) # <class 'bool'>

# Convert data types to Another Data Types
# age = float(age)
print(age)

gpa = int(gpa)
print(gpa)

age = str(age)
print(type(age), age)