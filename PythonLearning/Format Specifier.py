# Format specifier = {value : flags } format a value based on what flags are inserted

# .(number)f = round to that many decimal places (fixed point)
# :(number) = allocated that many spaces
# :03 = allocated and zero pad that many spaces
# :< = left justify
# :> = right justify
# :upper< = center align
# :+ = use a plus sign to indicate positive value
# := = place sign to left most position
# : = insert a space before positive number
# :, = comma separator


